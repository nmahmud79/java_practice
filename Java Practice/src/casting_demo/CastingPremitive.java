package casting_demo;

public class CastingPremitive {

	//Type casting: converting one data type to another is called Type Casting
	//for premitive date type---implicit (small to large) & explicit(large to small)
	public static void main(String[] args) {
		
		double x= 10.3;
		int y= (int)x;
		System.out.println(y);
		
	}

}
