package break_concept;
		
		//Java Break Statement in while loop
public class Break_Demo_2 {

	public static void main(String[] args) {
		
		int i= 1;
		//while loop
		while (i<=10) {
			
			if (i==5) {
				i++;
				break;
			}
		System.out.println(i);//See while loop in details
		i++;
		}
		
		
	}

}
