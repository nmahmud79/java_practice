	//Super keyword in java
	//Super keyword in java is a reference variable that is used to refer parent class object. 
	//>>Super is an implicit keyword create by JVM and supply each and every java program for performing important role in three places.

	//Super keyword At Variable Level
	//Super keyword At Method Level
	//Super keyword At Constructor Level	

	//Usage of Java super Keyword
	//1.super can be used to refer immediate parent class instance variable.
	//2.super can be used to invoke immediate parent class method (overriden method).
	//3.super() can be used to invoke immediate parent class constructor.

	//When Need of super keyword
	//Whenever the derived class is inherits the base class features, there is a possibility that base class features are similar to derived class features
	//>>and JVM gets an ambiguity. In order to differentiate between base class features and derived class features must be preceded by super keyword.

