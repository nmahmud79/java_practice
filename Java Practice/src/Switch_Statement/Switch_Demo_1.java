package Switch_Statement;

public class Switch_Demo_1 {

	public static void main(String[] args) {
		
		int number = 15;
		
		switch (number) {
		case 10:
			System.out.println("10");
			break;

		case 20:
			System.out.println("20");
			break;
			
		case 30:
			System.out.println("30");
			break;
			
		default:
			System.out.println("Not 10 or 20 or 30");
			break;
		}

	}

}
